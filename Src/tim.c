void Delay_us(uint16_t delay)
{
    __HAL_TIM_DISABLE(&htim3);
    __HAL_TIM_SET_COUNTER(&htim3,0);
    __HAL_TIM_ENABLE(&htim3);
    uint16_t curCnt=0;
    while(1)
    {
        curCnt=__HAL_TIM_GET_COUNTER(&htim3);
        if(curCnt>=delay)
            break;
    }
    __HAL_TIM_DISABLE(&htim3);
}
